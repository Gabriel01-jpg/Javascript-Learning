// Arredondamento de ponto flutuante down(baixo)
let num1 = 9.52578;
let num2 = Math.floor(num1);
console.log(num2);

// Arredondamento de ponto flutuante up(cima)
let num3 = Math.ceil(num1);
console.log(num3);

// Arrendodamento para o número mais próximo

let num4 = Math.round(num1);
console.log(num4);